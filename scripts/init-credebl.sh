# Kill all Credo controller containers that were spawned outside of docker compose.
# Uses two complementary strategies because the image-ancestor filter alone is unreliable:
#   1. Ancestor filter — works when the image tag hasn't changed.
#   2. Name pattern  — agent-provisioning always names these containers as
#      "{org-uuid}_{ContainerName}" (e.g. "c1f8a28f-71a3-..._Platform-admin").
#      Matching on the UUID prefix catches them regardless of image tag or pull history.
purge_stale_credo_containers() {
  local found=0 names

  # Strategy 1: by image ancestor
  names=$(docker ps -a \
    --filter "ancestor=ghcr.io/credebl/credo-controller:latest" \
    --format "{{.Names}}" 2>/dev/null || true)
  if [ -n "$names" ]; then
    echo "$names" | xargs docker rm -f 2>/dev/null || true
    echo "  Removed by image: $(printf '%s' "$names" | tr '\n' ' ')"
    found=1
  fi

  # Strategy 2: by name pattern — agent-provisioning names containers as
  # "{org-uuid}_{ContainerName}" (e.g. "c1f8a28f-..._Platform-admin").
  # Avoid grep-in-pipeline inside $() — with set -euo pipefail, grep's exit 1
  # (no match) can escape the || true guard and kill the script. Use a while-read
  # loop with bash regex instead, which has no pipeline exit-code hazard.
  names=""
  while IFS= read -r cname; do
    [[ "$cname" =~ ^[0-9a-f]{8}-[0-9a-f]{4}-[0-9a-f]{4}-[0-9a-f]{4}-[0-9a-f]{12}_ ]] \
      && names="${names}${cname}"$'\n' || true
  done < <(docker ps -a --format "{{.Names}}" 2>/dev/null || true)
  if [ -n "$names" ]; then
    printf '%s' "$names" | xargs docker rm -f 2>/dev/null || true
    echo "  Removed by name pattern: $(printf '%s' "$names" | tr '\n' ' ')"
    found=1
  fi

  [ "$found" -eq 0 ] && echo "  No stale Credo containers found." || true
}

clear_stale_platform_admin_agent() {
  # Remove the incomplete org_agents record so agent-service will try again.
  docker compose exec -T postgres env PGPASSWORD="$POSTGRES_PASSWORD" \
    psql -U credebl -d credebl -v ON_ERROR_STOP=1 -c "
      DELETE FROM org_agents oa
      USING organisation o
      WHERE oa.\"orgId\" = o.id
        AND o.name = 'Platform-admin'
        AND (
          oa.\"agentSpinUpStatus\" <> 2
          OR COALESCE(oa.\"agentEndPoint\", '') = ''
        );" >/dev/null

  # Credo (askar) creates a separate PostgreSQL database named after the wallet.
  # If a previous provision attempt failed mid-way, that database still exists and
  # the next attempt throws "Your wallet is already been created". Drop it so the
  # next agent-service startup can provision the wallet cleanly from scratch.
  docker compose exec -T postgres env PGPASSWORD="$POSTGRES_PASSWORD" \
    psql -U credebl -d postgres -c "
      SELECT pg_terminate_backend(pid)
      FROM pg_stat_activity
      WHERE datname = '${PLATFORM_WALLET_NAME}';
      DROP DATABASE IF EXISTS \"${PLATFORM_WALLET_NAME}\";" >/dev/null 2>&1 || true
}

# Handles the "container running but endpoint never written" race condition.
# CREDEBL's agent-service can restart mid-provisioning, lose the NATS response from
# agent-provisioning, and leave agentSpinUpStatus=1 with an empty agentEndPoint even
# though the Credo container is healthy. This function detects that state, extracts the
# admin port from the running container, tests it, and patches the DB directly.
recover_orphan_running_agent() {
  local container port endpoint

  # Look for a running (not exited) Credo container whose name matches the UUID pattern
  container=$(docker ps --format "{{.Names}}" 2>/dev/null \
    | grep -E "^[0-9a-f]{8}-[0-9a-f]{4}-[0-9a-f]{4}-[0-9a-f]{4}-[0-9a-f]{12}_Platform-admin$" \
    | head -1 || true)
  [ -z "$container" ] && return 1

  echo "  Found running agent container: $container"

  # Extract the host-side admin port (8xxx range — that's the Credo REST API port)
  # `docker port` output: "8006/tcp -> 0.0.0.0:8006" — $NF is always the host port
  port=$(docker port "$container" 2>/dev/null \
    | awk -F'[/: ]' '$1+0 >= 8000 && $1+0 < 9000 { print $NF; exit }')
  [ -z "$port" ] && { echo "  Could not determine admin port for $container"; return 1; }

  endpoint="http://${VPS_HOST}:${port}"
  echo "  Testing agent endpoint: ${endpoint}/agent/token"

  if ! curl -sf --max-time 10 -X POST \
      -H "Authorization: $AGENT_API_KEY" "${endpoint}/agent/token" >/dev/null; then
    echo "  Agent container is running but not responding on ${endpoint}"
    return 1
  fi

  echo "  Agent is responding. Patching DB record (status=2, endpoint=${endpoint})..."
  docker compose exec -T postgres env PGPASSWORD="$POSTGRES_PASSWORD" \
    psql -U credebl -d credebl -v ON_ERROR_STOP=1 -c "
      UPDATE org_agents oa
      SET \"agentSpinUpStatus\" = 2,
          \"agentEndPoint\"     = '${endpoint}'
      FROM organisation o
      WHERE oa.\"orgId\" = o.id
        AND o.name = 'Platform-admin';" >/dev/null

  echo "  Restarting agent-service so it picks up the updated endpoint..."
  docker compose restart agent-service >/dev/null
  sleep 20
}

# Keycloak 24+ only includes `sub` in access tokens when the `openid` scope is
# present in the realm. On a plain realm import the scope may be absent because
# Keycloak normally auto-creates it, but skips that step when importing from JSON.
# Without `sub`, CREDEBL cannot perform the keycloakUserId lookup and every
# profile/org endpoint returns 404 "User not found".
# This function is idempotent: it checks before creating or linking.
ensure_keycloak_openid_scope() {
  echo
  echo "Ensuring Keycloak openid scope (sub claim) is present..."

  local token scope_id client_id

  # Get admin token from Keycloak master realm
  token=$(curl -sf --max-time 15 -X POST \
    "http://localhost:8080/realms/master/protocol/openid-connect/token" \
    -H "Content-Type: application/x-www-form-urlencoded" \
    -d "client_id=admin-cli&username=${KEYCLOAK_ADMIN_USER:-admin}&password=${KEYCLOAK_ADMIN_PASSWORD}&grant_type=password" \
    | python3 -c 'import json,sys; print(json.load(sys.stdin)["access_token"])' 2>/dev/null) || true
  if [ -z "$token" ]; then
    echo "  Warning: could not get Keycloak admin token — skipping openid scope check." >&2
    return 0
  fi

  # Check if scope already exists
  scope_id=$(curl -sf --max-time 10 \
    "http://localhost:8080/admin/realms/credebl-realm/client-scopes" \
    -H "Authorization: Bearer ${token}" \
    | python3 -c 'import json,sys; s=[x["id"] for x in json.load(sys.stdin) if x["name"]=="openid"]; print(s[0] if s else "")' 2>/dev/null) || true

  if [ -z "$scope_id" ]; then
    echo "  openid scope missing — creating with oidc-sub-mapper..."
    curl -sf --max-time 15 -X POST \
      "http://localhost:8080/admin/realms/credebl-realm/client-scopes" \
      -H "Authorization: Bearer ${token}" \
      -H "Content-Type: application/json" \
      -d '{
        "name": "openid",
        "description": "OpenID Connect built-in scope",
        "protocol": "openid-connect",
        "attributes": {"include.in.token.scope": "true"},
        "protocolMappers": [{
          "name": "sub",
          "protocol": "openid-connect",
          "protocolMapper": "oidc-sub-mapper",
          "consentRequired": false,
          "config": {"access.token.sub.claim": "true", "id.token.sub.claim": "true"}
        }]
      }' >/dev/null

    scope_id=$(curl -sf --max-time 10 \
      "http://localhost:8080/admin/realms/credebl-realm/client-scopes" \
      -H "Authorization: Bearer ${token}" \
      | python3 -c 'import json,sys; s=[x["id"] for x in json.load(sys.stdin) if x["name"]=="openid"]; print(s[0] if s else "")' 2>/dev/null) || true

    [ -z "$scope_id" ] && { echo "  Warning: failed to create openid scope." >&2; return 0; }
    echo "  openid scope created (id: ${scope_id})."
  else
    echo "  openid scope already present (id: ${scope_id})."
  fi

  # Add as default scope to credebl-client and adminClient (idempotent — PUT is no-op if already linked)
  for client_name in credebl-client adminClient; do
    client_id=$(curl -sf --max-time 10 \
      "http://localhost:8080/admin/realms/credebl-realm/clients?clientId=${client_name}" \
      -H "Authorization: Bearer ${token}" \
      | python3 -c 'import json,sys; d=json.load(sys.stdin); print(d[0]["id"] if d else "")' 2>/dev/null) || true
    if [ -n "$client_id" ]; then
      curl -sf --max-time 10 -X PUT \
        "http://localhost:8080/admin/realms/credebl-realm/clients/${client_id}/default-client-scopes/${scope_id}" \
        -H "Authorization: Bearer ${token}" >/dev/null || true
      echo "  openid scope linked to ${client_name}."
    fi
  done
}

ensure_keycloak_admin_client_redirect_uri() {
  echo
  echo "Ensuring adminClient has Studio redirect URI (required for verification email)..."

  local token client_id current_uris

  token=$(curl -sf --max-time 15 -X POST \
