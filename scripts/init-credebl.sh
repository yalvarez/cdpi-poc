ensure_sendgrid_from_address() {
  # The FROM address used at email-send time is read from platform_config.emailFrom
  # in Postgres — NOT from the EMAIL_FROM env var. The seed populates this column
  # with whatever default it has compiled in (noreply@cdpi-poc.local), regardless
  # of what EMAIL_FROM is set to in .env.
  #
  # For SendGrid to accept the outbound email, emailFrom MUST be a verified sender
  # address in the SendGrid account. This function updates the DB row directly.
  echo
  echo "Updating platform_config.emailFrom for SendGrid..."
  local current
  current="$(docker compose exec -T postgres env PGPASSWORD="$POSTGRES_PASSWORD" \
    psql -U credebl -d credebl -Atqc 'SELECT "emailFrom" FROM platform_config LIMIT 1;' \
    2>/dev/null | tr -d '\r')"
  if [ "$current" = "$EMAIL_FROM_VAL" ]; then
    echo "  emailFrom already set to: ${EMAIL_FROM_VAL} — skipping."
    return 0
  fi
  docker compose exec -T postgres env PGPASSWORD="$POSTGRES_PASSWORD" \
    psql -U credebl -d credebl -v ON_ERROR_STOP=1 -c \
    "UPDATE platform_config SET \"emailFrom\" = '${EMAIL_FROM_VAL}';" >/dev/null
  echo "  platform_config.emailFrom updated: ${current} → ${EMAIL_FROM_VAL}"
  # Restart user + issuance so any in-memory cached config is invalidated
  docker compose restart user issuance >/dev/null
  sleep 10
  echo "  User and issuance services restarted."
}

ensure_brand_logo() {
  # Upload CREDEBL logo to MinIO orgLogos/ and set BRAND_LOGO in .env.
  # The issuance email template uses BRAND_LOGO for the header image.
  # Without it the email shows a broken <img src="undefined"> tag.
  if grep -q '^BRAND_LOGO=' "$ENV_FILE" 2>/dev/null; then
    echo "  BRAND_LOGO already configured — skipping logo upload."
    return 0
  fi
  echo "  Uploading CREDEBL logo to MinIO..."
  # Copy logo from Studio image (always present) to MinIO orgLogos/
  docker cp credebl-studio:/app/public/images/CREDEBL_LOGO.png /tmp/credebl-logo-upload.png 2>/dev/null
  if [ ! -f /tmp/credebl-logo-upload.png ]; then
    echo "  WARNING: Could not find logo in Studio container — skipping."
    return 0
  fi
  docker cp /tmp/credebl-logo-upload.png credebl-minio:/tmp/credebl-logo-upload.png 2>/dev/null
  docker exec credebl-minio mc alias set cdpi http://localhost:9000 \
    "$MINIO_ROOT_USER" "$MINIO_ROOT_PASSWORD" --quiet 2>/dev/null
  docker exec credebl-minio mc cp /tmp/credebl-logo-upload.png \
    cdpi/credebl-bucket/orgLogos/credebl-logo.png 2>/dev/null
  rm -f /tmp/credebl-logo-upload.png
  LOGO_URL="http://${VPS_IP}:9000/credebl-bucket/orgLogos/credebl-logo.png"
  printf '\n# Email branding — set by init-credebl.sh\nBRAND_LOGO=%s\nIOS_DOWNLOAD_LINK=https://apps.apple.com/in/app/inji-wallet/id1631979601\n' "$LOGO_URL" >> "$ENV_FILE"
  echo "  Logo available at: $LOGO_URL"
  echo "  BRAND_LOGO and IOS_DOWNLOAD_LINK added to .env — restarting issuance..."
  # Use docker restart (not compose up --force-recreate) to avoid cascading other containers
  # and losing in-container patches that were applied earlier.
  docker restart credebl-issuance >/dev/null 2>&1 &
  sleep 5
}

apply_container_patches() {
  echo
  echo "Applying CREDEBL container patches..."
  echo -n "  Utility service S3 → MinIO endpoint: "
  patch_utility_s3
  docker compose restart utility >/dev/null

  echo -n "  API gateway @context validator (require_tld): "
  patch_api_gateway_context_validator
  docker compose restart api-gateway >/dev/null

  echo -n "  Issuance service schema URL deduplication: "
  patch_issuance_schema_url
  echo -n "  Issuance service @context URL normalization: "
  patch_issuance_context_urls
  docker compose restart issuance >/dev/null

  echo -n "  Agent-service shared wallet create-tenant (root JWT): "
  patch_agent_service_create_tenant
  docker compose restart agent-service >/dev/null

  echo "  Waiting for restarted containers to be ready..."
  local deadline=$(( $(date +%s) + 90 ))
  while [ "$(date +%s)" -lt "$deadline" ]; do
    local gw_health as_log
