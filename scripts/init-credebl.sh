require_cmd() {
  command -v "$1" >/dev/null 2>&1 || { echo "Error: required command '$1' not found." >&2; exit 1; }
}

# Strip protocol and trailing slashes from a host input
sanitize_host() {
  local v
  v="$(printf '%s' "$1" | sed 's/^ *//;s/ *$//')"
  v="${v#http://}"
  v="${v#https://}"
  v="${v%%/*}"
  printf '%s' "$v"
}

ask() {
  local prompt="$1"
  local default="${2:-}"
  local secret="${3:-false}"
  local value

  while true; do
    if [ "$secret" = "true" ]; then
      [ -n "$default" ] \
        && printf "%s [press Enter for auto-generated value]: " "$prompt" >&2 \
        || printf "%s: " "$prompt" >&2
      read -r -s value
      printf "\n" >&2
    else
      [ -n "$default" ] \
        && printf "%s [%s]: " "$prompt" "$default" >&2 \
        || printf "%s: " "$prompt" >&2
      read -r value
    fi

    [ -z "$value" ] && value="$default"
    [ -n "$value" ] && { printf '%s' "$value"; return 0; }
    echo "A value is required." >&2
  done
}

ask_yes_no() {
  local prompt="$1"
  local default="${2:-N}"
  local reply suffix="[y/N]"
  [ "$default" = "Y" ] && suffix="[Y/n]"

  while true; do
    printf "%s %s: " "$prompt" "$suffix" >&2
    read -r reply
    reply="$(printf '%s' "$reply" | sed 's/^ *//;s/ *$//')"
    [ -z "$reply" ] && reply="$default"
    case "$reply" in
      [Yy]|[Yy][Ee][Ss]) return 0 ;;
      [Nn]|[Nn][Oo])     return 1 ;;
    esac
    echo "Please answer yes or no." >&2
  done
}

