wait_for_container_state() {
  local name="$1" pattern="$2" attempts="${3:-60}" i=1
  while [ "$i" -le "$attempts" ]; do
    docker inspect --format='{{.State.Status}} {{.State.ExitCode}}' "$name" 2>/dev/null \
      | grep -Eqi "$pattern" && return 0
    sleep 2
    i=$((i + 1))
  done
  return 1
}

# =============================================================================
# CONTAINER PATCHES
# =============================================================================
# CREDEBL container patches required for self-hosted MinIO and internal schemas.
# Applied after every `docker compose up` because patches live inside container
# filesystems and are lost when containers are recreated.
# Each patch is idempotent — safe to run on already-patched containers.

patch_utility_s3() {
  # AWS SDK v2 does NOT read AWS_ENDPOINT from environment automatically.
  # Without this patch, S3 calls from the utility service go to real AWS
  # and fail with "The AWS Access Key Id you provided does not exist".
  # We add endpoint + s3ForcePathStyle:true to all three S3 client constructors.
  # Uses docker cp + --user root because the process runs as uid=1001 (read-only /app).
  local patch_script
  patch_script="$(mktemp /tmp/patch_utility_XXXXXX.js)"
  cat > "$patch_script" << 'JSEOF'
const fs = require('fs');
const path = '/app/dist/apps/utility/main.js';
let content = fs.readFileSync(path, 'utf8');
if (content.includes('s3ForcePathStyle')) { process.stdout.write('already patched\n'); process.exit(0); }
content = content
  .replace(
    'region: process.env.AWS_REGION\n        });',
    'region: process.env.AWS_REGION,\n            endpoint: process.env.AWS_ENDPOINT,\n            s3ForcePathStyle: true\n        });'
  )
  .replace(
    'region: process.env.AWS_PUBLIC_REGION\n        });',
    'region: process.env.AWS_PUBLIC_REGION,\n            endpoint: process.env.AWS_ENDPOINT,\n            s3ForcePathStyle: true\n        });'
  )
  .replace(
    'region: process.env.AWS_S3_STOREOBJECT_REGION\n        });',
    'region: process.env.AWS_S3_STOREOBJECT_REGION,\n            endpoint: process.env.AWS_S3_STOREOBJECT_ENDPOINT || process.env.AWS_ENDPOINT,\n            s3ForcePathStyle: true\n        });'
  );
if (!content.includes('s3ForcePathStyle')) { process.stderr.write('ERROR: patch pattern not found — CREDEBL image may have changed\n'); process.exit(1); }
fs.writeFileSync(path, content);
process.stdout.write('patched\n');
JSEOF
  docker cp "$patch_script" credebl-utility:/tmp/patch_utility.js
  rm -f "$patch_script"
  docker exec --user root credebl-utility node /tmp/patch_utility.js
}

patch_api_gateway_context_validator() {
  # IsCredentialJsonLdContext validator calls isURL(v) with default require_tld:true.
  # This rejects Docker-internal hostnames like schema-file-server (no TLD).
  # Adding require_tld:false allows internal hostnames in @context arrays.
  #
  # Implementation notes:
  # - The api-gateway process runs as uid=1001 (nextjs), not root — cannot write to /app.
  # - Heredoc via `docker exec ... node -` is unreliable inside SSH quoted commands.
  # - Solution: write the patch script to a temp file on the host, docker cp it in,
  #   then exec as --user root.
  local patch_script
  patch_script="$(mktemp /tmp/patch_api_gw_XXXXXX.js)"
  cat > "$patch_script" << 'JSEOF'
const fs = require('fs');
const path = '/app/dist/apps/api-gateway/main.js';
let content = fs.readFileSync(path, 'utf8');
const target = '(0, class_validator_1.isURL)(v)';
const replacement = '(0, class_validator_1.isURL)(v, { require_tld: false })';
// Find the FUNCTION DEFINITION — the decorator usage appears ~33k chars earlier in the bundle
// so we must anchor to 'function IsCredentialJsonLdContext', not just 'IsCredentialJsonLdContext'.
const funcIdx = content.indexOf('function IsCredentialJsonLdContext');
if (funcIdx < 0) { process.stderr.write('ERROR: function IsCredentialJsonLdContext not found\n'); process.exit(1); }
const after = content.indexOf(target, funcIdx);
// The isURL call is ~500 chars after the function definition
if (after < 0 || after > funcIdx + 1000) { process.stdout.write('already patched\n'); process.exit(0); }
content = content.substring(0, after) + replacement + content.substring(after + target.length);
fs.writeFileSync(path, content);
process.stdout.write('patched\n');
JSEOF
  docker cp "$patch_script" credebl-api-gateway:/tmp/patch_api_gw.js
  rm -f "$patch_script"
  docker exec --user root credebl-api-gateway node /tmp/patch_api_gw.js
}

patch_credo_credential_events() {
  # The Credo root agent (multi-tenancy) does not expose agent.modules.credentials.
  # Without this patch, getFormatData() throws "Cannot read properties of undefined"
  # which crashes the event handler and causes ECONNREFUSED on retries.
  local credo_container
  credo_container="$(docker ps --format '{{.Names}}' | grep -v '^credebl-' | grep -v '^[0-9a-f]\{12\}_credebl' | head -1)"
  if [ -z "$credo_container" ]; then
    echo "  No Credo controller container found — skipping."
    return 0
  fi
  # Credo containers also run as uid=1001 — use docker cp + --user root.
  local patch_script
  patch_script="$(mktemp /tmp/patch_credo_XXXXXX.js)"
  cat > "$patch_script" << 'JSEOF'
const fs = require('fs');
const path = '/app/build/events/CredentialEvents.js';
let content = fs.readFileSync(path, 'utf8');
if (content.includes('getFormatData unavailable')) { process.stdout.write('already patched\n'); process.exit(0); }
const pattern = /^(\s+)const data = await agent\.modules\.credentials\.getFormatData\(record\.id\);\n\s+body\.credentialData = data;/m;
const m = content.match(pattern);
if (!m) { process.stderr.write('ERROR: patch target not found in CredentialEvents.js\n'); process.exit(1); }
const indent = m[1];
const replacement = `${indent}try {\n${indent}    if (agent.modules && agent.modules.credentials) {\n${indent}        const data = await agent.modules.credentials.getFormatData(record.id);\n${indent}        body.credentialData = data;\n${indent}    }\n${indent}} catch (e) {\n${indent}    // getFormatData unavailable in this agent context (e.g. multi-tenancy root agent)\n${indent}}`;
content = content.replace(pattern, replacement);
fs.writeFileSync(path, content);
process.stdout.write('patched\n');
JSEOF
  docker cp "$patch_script" "${credo_container}:/tmp/patch_credo.js"
  rm -f "$patch_script"
  local result
  result="$(docker exec --user root "$credo_container" node /tmp/patch_credo.js)"
  printf '%s\n' "$result"
  # Only restart Credo when the patch was actually applied — a restart regenerates
  # Credo's random secretKey, invalidating the stored tenant JWT. Skipping the
  # restart when already patched avoids a pointless JWT refresh cycle on re-runs.
  if [ "$result" = "patched" ]; then
    docker restart "$credo_container" >/dev/null
  fi
}

patch_credo_proof_events() {
  # Patch 7 — Credo ProofEvents.js crash in multi-tenancy root agent
  #
  # Two bugs in ProofEvents.js crash Credo when an OOB proof request is created:
  #
  # Bug A: contextCorrelationId starts with "tenant-" (it is a context correlation ID,
  # not a bare tenant UUID). getTenantAgent({tenantId: "tenant-abc..."}) throws:
  #   "Tenant id already starts with 'tenant-'. You are probably passing a context
  #    correlation id"
  # Fix A: strip the "tenant-" prefix before passing to getTenantAgent.
  #
  # Bug B: tenantAgent.proofs is undefined in the multi-tenancy root agent context.
  # Calling tenantAgent.proofs.getFormatData(record.id) throws TypeError, crashing
  # the event handler and causing ECONNRESET on the proof request.
  # Fix B: wrap the getFormatData call in try-catch (same pattern as Patch 3).
  #
  # _Symptom if missing_: POST /orgs/{id}/proofs/oob returns 500 "Something went wrong!"
  # with Credo log: CredoError "Tenant id already starts with 'tenant-'" → crash →
  # restart; after Fix A applied but before Fix B: TypeError "Cannot read properties of
  # undefined (reading 'getFormatData')" → crash → restart.
  local credo_container
  credo_container="$(docker ps --format '{{.Names}}' | grep -v '^credebl-' | grep -v '^[0-9a-f]\{12\}_credebl' | head -1)"
  if [ -z "$credo_container" ]; then
    echo "  No Credo controller container found — skipping."
    return 0
  fi
  local patch_script
  patch_script="$(mktemp /tmp/patch_proof_XXXXXX.js)"
  cat > "$patch_script" << 'JSEOF'
const fs = require('fs');
const path = '/app/build/events/ProofEvents.js';
let c = fs.readFileSync(path, 'utf8');
if (c.includes('proofData try-catch guard')) { process.stdout.write('already patched\n'); process.exit(0); }
// Fix A: strip "tenant-" prefix from contextCorrelationId before getTenantAgent
const tenantIdTarget = 'tenantId: event.metadata.contextCorrelationId,';
if (c.indexOf(tenantIdTarget) >= 0) {
  c = c.replace(tenantIdTarget,
    "tenantId: event.metadata.contextCorrelationId.indexOf('tenant-') === 0 ? event.metadata.contextCorrelationId.slice(7) : event.metadata.contextCorrelationId, // tenant- prefix guard");
}
// Fix B: wrap tenantAgent.proofs.getFormatData in try-catch
const getFormatTarget = 'const data = await tenantAgent.proofs.getFormatData(record.id);\n            body.proofData = data;';
if (c.indexOf(getFormatTarget) >= 0) {
  c = c.replace(getFormatTarget,
    'try { if (tenantAgent && tenantAgent.proofs) { const data = await tenantAgent.proofs.getFormatData(record.id); body.proofData = data; } } catch (e) { /* proofData try-catch guard */ }');
}
if (!c.includes('proofData try-catch guard')) { process.stderr.write('ERROR: patch target not found in ProofEvents.js\n'); process.exit(1); }
fs.writeFileSync(path, c);
process.stdout.write('patched\n');
JSEOF
  docker cp "$patch_script" "${credo_container}:/tmp/patch_proof.js"
  rm -f "$patch_script"
  local result
  result="$(docker exec --user root "$credo_container" node /tmp/patch_proof.js)"
  printf '%s\n' "$result"
  if [ "$result" = "patched" ]; then
    docker restart "$credo_container" >/dev/null
  fi
}

patch_issuance_schema_url() {
  # Studio's URL builder function incorrectly prepends http:// to URLs that already
  # have it (regex matches http://host:port/schemas/ as host:port/schemas/).
  # This patch strips duplicate http:// prefixes in getW3CSchemaAttributes before fetch.
  # Uses indexOf instead of regex literal to avoid escaping issues in heredoc.
  # Uses docker cp + --user root (issuance runs as uid=1001, read-only /app).
  local patch_script
  patch_script="$(mktemp /tmp/patch_issuance_XXXXXX.js)"
  cat > "$patch_script" << 'JSEOF'
const fs = require('fs');
const path = '/app/dist/apps/issuance/main.js';
let content = fs.readFileSync(path, 'utf8');
if (content.includes('indexOf("://http")')) { process.stdout.write('already patched\n'); process.exit(0); }
const target = 'async getW3CSchemaAttributes(schemaUrl) {';
if (!content.includes(target)) { process.stderr.write('ERROR: patch target not found\n'); process.exit(1); }
// Use slice+indexOf only — NO regex literals. A regex literal /^https?:\/\// inserted via
// string concatenation becomes /^https?:///, where // terminates the regex prematurely,
// causing "SyntaxError: missing ) after argument list" when the patched bundle is loaded.
const fixLines = ' while (schemaUrl && schemaUrl.indexOf("://http") > 0) { schemaUrl = schemaUrl.slice(schemaUrl.indexOf("://") + 3); }';
content = content.replace(target, target + fixLines);
fs.writeFileSync(path, content);
process.stdout.write('patched\n');
JSEOF
  docker cp "$patch_script" credebl-issuance:/tmp/patch_issuance.js
  rm -f "$patch_script"
  docker exec --user root credebl-issuance node /tmp/patch_issuance.js
}

patch_issuance_context_urls() {
  # Studio's credential offer builder triple-prepends http:// to @context URLs,
  # producing "http://http://http://schema-file-server:4000/schemas/...".
  # Credo rejects these malformed URLs with 400 "@context must be an array of strings".
  # This patch normalizes @context URL arrays in outOfBandCredentialOffer right
  # after the JSONLD branch validates/updates issuance dates.
  # Uses indexOf-based loop (no regex literals) and docker cp + --user root.
  local patch_script
  patch_script="$(mktemp /tmp/patch_issuance_ctx_XXXXXX.js)"
  cat > "$patch_script" << 'JSEOF'
const fs = require('fs');
const path = '/app/dist/apps/issuance/main.js';
let content = fs.readFileSync(path, 'utf8');
if (content.includes('ctx.map(function(url)')) { process.stdout.write('already patched\n'); process.exit(0); }
const target = "'Validated/Updated Issuance dates credential offer'";
const idx = content.indexOf(target);
if (idx < 0) { process.stderr.write('ERROR: patch target not found in issuance/main.js\n'); process.exit(1); }
// Insert normalization right after the logger.debug call's closing semicolon
const insertAfter = content.indexOf(';', idx) + 1;
const normCode = ' if (credentialOffer) { for (const offer of credentialOffer) { const ctx = offer && offer.credential && offer.credential["@context"]; if (Array.isArray(ctx)) { offer.credential["@context"] = ctx.map(function(url) { while (typeof url === "string" && url.indexOf("://http") > 0) { url = url.slice(url.indexOf("://") + 3); } return url; }); } } }';
content = content.substring(0, insertAfter) + normCode + content.substring(insertAfter);
fs.writeFileSync(path, content);
process.stdout.write('patched\n');
JSEOF
  docker cp "$patch_script" credebl-issuance:/tmp/patch_issuance_ctx.js
  rm -f "$patch_script"
  docker exec --user root credebl-issuance node /tmp/patch_issuance_ctx.js
}

