  fi

  # Check if redirect URI already set
  current_uris=$(curl -sf --max-time 10 \
    "http://localhost:8080/admin/realms/credebl-realm/clients/${client_id}" \
    -H "Authorization: Bearer ${token}" \
    | python3 -c 'import json,sys; print(json.load(sys.stdin).get("redirectUris","[]"))' 2>/dev/null) || true

  if echo "$current_uris" | grep -qF "${STUDIO_URL}"; then
    echo "  adminClient redirect URI already set."
    return 0
  fi

  curl -sf --max-time 15 -X PUT \
    "http://localhost:8080/admin/realms/credebl-realm/clients/${client_id}" \
    -H "Authorization: Bearer ${token}" \
    -H "Content-Type: application/json" \
    -d "{\"redirectUris\": [\"${STUDIO_URL}/*\"]}" >/dev/null
  echo "  adminClient redirect URI set to ${STUDIO_URL}/*"

  # Grant realm-management roles to adminClient service account.
  # The user service uses adminClient credentials to call the Keycloak Admin API for:
  #   - GET  /clients              (view-clients)
  #   - POST /users                (manage-users)
  #   - GET  /users, PUT /users/*  (view-users, manage-users)
  #   - query-users, query-clients — needed for search endpoints
  local sa_user_id rm_client_id role_json role_id role_name
  sa_user_id=$(curl -sf --max-time 10 \
    "http://localhost:8080/admin/realms/credebl-realm/clients/${client_id}/service-account-user" \
    -H "Authorization: Bearer ${token}" \
    | python3 -c 'import json,sys; print(json.load(sys.stdin)["id"])' 2>/dev/null) || true

  rm_client_id=$(curl -sf --max-time 10 \
    "http://localhost:8080/admin/realms/credebl-realm/clients?clientId=realm-management" \
    -H "Authorization: Bearer ${token}" \
    | python3 -c 'import json,sys; print(json.load(sys.stdin)[0]["id"])' 2>/dev/null) || true

  if [ -z "$sa_user_id" ] || [ -z "$rm_client_id" ]; then
    echo "  Warning: could not resolve service account or realm-management client — skipping role grant." >&2
  else
    role_json="["
    for role_name in manage-users view-users query-users manage-clients view-clients query-clients manage-realm view-realm; do
      role_id=$(curl -sf --max-time 10 \
        "http://localhost:8080/admin/realms/credebl-realm/clients/${rm_client_id}/roles/${role_name}" \
        -H "Authorization: Bearer ${token}" \
        | python3 -c 'import json,sys; print(json.load(sys.stdin)["id"])' 2>/dev/null) || true
      [ -n "$role_id" ] && role_json="${role_json}{\"id\":\"${role_id}\",\"name\":\"${role_name}\"},"
    done
    role_json="${role_json%,}]"
    curl -sf --max-time 10 -X POST \
      "http://localhost:8080/admin/realms/credebl-realm/users/${sa_user_id}/role-mappings/clients/${rm_client_id}" \
      -H "Authorization: Bearer ${token}" \
      -H "Content-Type: application/json" \
      -d "${role_json}" >/dev/null || true
    echo "  adminClient service account granted realm-management roles."
  fi
}

ensure_platform_admin_shared_agent() {
  echo
  echo "Ensuring platform-admin shared agent is initialized..."

  if ! wait_for_container_state "credebl-platform-admin-bootstrap" '^exited 0$' 90; then
    echo "Error: platform-admin-bootstrap did not complete." >&2
    echo "Inspect with: docker compose logs --tail=200 platform-admin-bootstrap" >&2
    return 1
  fi

  # Check if already ready on first pass (happy path — fresh deploy with no prior failures).
  if platform_admin_shared_agent_ready; then
    echo "Platform-admin shared agent is ready."
    return 0
  fi

  # Before entering the retry loop: the initial docker compose up may have already
  # spawned a Credo container that is healthy but whose endpoint was never written to
  # the DB (the NATS response was lost because agent-service started too fast). Check
  # for that orphan first — if it's responding, patch the DB and skip a full re-provision.
  echo "  Initial check failed. Looking for an orphan Credo container from the first attempt..."
  local orphan_wait=0
  while [ "$orphan_wait" -lt 120 ]; do
    if recover_orphan_running_agent && platform_admin_shared_agent_ready; then
      echo "Platform-admin shared agent is ready (recovered orphan from initial attempt)."
      return 0
    fi
    sleep 15
    orphan_wait=$((orphan_wait + 15))
    echo "  Waiting for Credo to become responsive... (${orphan_wait}s / 120s)"
  done

  local attempt=1
  while [ "$attempt" -le 6 ]; do
    echo "  Not ready yet (attempt $attempt/6). Clearing stale state and restarting..."
    clear_stale_platform_admin_agent || true
    purge_stale_credo_containers
    docker compose up -d cloud-wallet >/dev/null

    # Fix NATS race: restart agent-provisioning first and wait until it has registered
    # its 'wallet-provisioning' subscription before starting agent-service.
    # Without this sequencing, agent-service calls NATS ~12s after start, but
    # agent-provisioning doesn't connect until ~17s — NATS returns "No Responders"
    # immediately and the provisioning attempt fails silently ({} error).
    echo "  Restarting agent-provisioning and waiting for NATS subscription..."
    docker compose restart agent-provisioning >/dev/null
    local nats_wait=0
    while [ "$nats_wait" -lt 45 ]; do
      sleep 5
      nats_wait=$((nats_wait + 5))
      if docker compose logs --since 45s agent-provisioning 2>/dev/null \
          | grep -q "Microservice is listening"; then
        echo "  agent-provisioning is listening on NATS (${nats_wait}s)."
        break
      fi
    done

    echo "  Starting agent-service..."
    docker compose restart agent-service >/dev/null

    # Full provision cycle: agent-service NestJS startup (~10s) + NATS call +
    # docker_start_agent.sh shell script (~60s fixed wait) + Credo init (~60s).
    echo "  Waiting 130s for full provisioning cycle (shell script + Credo startup)..."
    sleep 130

    # Check DB first; if status=2 and endpoint set, also verify Credo responds.
    if platform_admin_shared_agent_ready; then
      echo "Platform-admin shared agent is ready."
      return 0
    fi

    # Credo may still be initializing — give it up to 90s more before giving up on this attempt.
    local extra=0
    while [ "$extra" -lt 90 ]; do
      sleep 15
      extra=$((extra + 15))
      if recover_orphan_running_agent && platform_admin_shared_agent_ready; then
        echo "Platform-admin shared agent is ready (recovered after retry $attempt)."
        return 0
      fi
      if platform_admin_shared_agent_ready; then
        echo "Platform-admin shared agent is ready."
        return 0
      fi
      echo "  Still waiting for Credo... (${extra}s extra)"
    done

    attempt=$((attempt + 1))
  done

  echo "Error: platform-admin shared agent did not reach ready state after all attempts." >&2
  echo "Inspect with: docker compose logs --tail=200 agent-service agent-provisioning" >&2
  return 1
}

# Create a multi-tenancy tenant for the Platform-admin org inside the shared Credo
# agent and store the encrypted tenant JWT in org_agents.apiKey. This is required
# because CREDEBL's agent-service (DEDICATED type) passes the decrypted apiKey
# directly as the Authorization header on Credo calls — Credo routes by the
# tenantId embedded in the RestTenantAgent JWT.
#
# The tenant JWT is signed with Credo's random secretKey (generated at container
# startup and stored in Askar wallet). It does NOT expire (no exp claim) and
# remains valid as long as the Credo container is not restarted. If the Credo
# container is ever restarted, re-run this function to refresh the stored JWT.
ensure_platform_admin_tenant() {
  echo
  echo "Ensuring Platform-admin tenant wallet is set up in the Credo agent..."

  # Get the agent endpoint from DB
  local endpoint tenant_id
  endpoint="$(docker compose exec -T postgres env PGPASSWORD="$POSTGRES_PASSWORD" \
    psql -U credebl -d credebl -Atqc "
      SELECT oa.\"agentEndPoint\" FROM org_agents oa
      JOIN organisation o ON o.id = oa.\"orgId\"
      WHERE o.name = 'Platform-admin' AND oa.\"agentSpinUpStatus\" = 2
      LIMIT 1;" 2>/dev/null | tr -d '\r')"

  if [ -z "$endpoint" ]; then
    echo "  Error: Platform-admin agent endpoint not found in DB." >&2
    return 1
  fi

  # Normalize endpoint: Credo admin ports only serve HTTP.
  # Strip https:// if present (API_GATEWAY_PROTOCOL=https can cause it to be stored
  # with https:// when SSL is enabled), then ensure http:// prefix.
  [[ "$endpoint" == https://* ]] && endpoint="http://${endpoint#https://}"
  [[ "$endpoint" =~ ^http:// ]] || endpoint="http://${endpoint}"
  # Also fix the DB record if it was stored with https://
  docker compose exec -T postgres env PGPASSWORD="$POSTGRES_PASSWORD" \
    psql -U credebl -d credebl -q -c \
    "UPDATE org_agents SET \"agentEndPoint\" = replace(\"agentEndPoint\", 'https://', 'http://')
     WHERE \"agentEndPoint\" LIKE 'https://%';" 2>/dev/null || true

  # Get Credo root JWT — retry up to 8 times (40s total) to handle Credo still
  # starting up after being restarted by patch_credo_credential_events.
  # Credo's /agent/token returns {"token":"..."} — NOT {"access_token":"..."}.
  local root_jwt attempt=1
  while [ "$attempt" -le 8 ]; do
    root_jwt="$(curl -sf --max-time 10 -X POST \
      -H "Authorization: $AGENT_API_KEY" \
      "${endpoint}/agent/token" 2>/dev/null \
      | python3 -c "import sys,json; d=json.load(sys.stdin); print(d.get('token', d.get('access_token','')))" 2>/dev/null || true)"
    [ -n "$root_jwt" ] && break
    echo "  Credo not ready yet (attempt ${attempt}/8), waiting 5s..."
    sleep 5
    attempt=$((attempt + 1))
  done

  if [ -z "$root_jwt" ]; then
    echo "  Error: Failed to get root JWT from Credo agent at ${endpoint} after retries." >&2
    return 1
  fi

  # Check if tenant already exists in DB
  tenant_id="$(docker compose exec -T postgres env PGPASSWORD="$POSTGRES_PASSWORD" \
    psql -U credebl -d credebl -Atqc "
      SELECT COALESCE(oa.\"tenantId\", '') FROM org_agents oa
      JOIN organisation o ON o.id = oa.\"orgId\"
      WHERE o.name = 'Platform-admin'
      LIMIT 1;" 2>/dev/null | tr -d '\r')"

  if [ -z "$tenant_id" ]; then
    # Create tenant in Credo.
    # Payload: only "config" — the "jwt" field is excess and rejected by Credo's validator.
    # Response: {"id":"<tenantId>","token":"<RestTenantAgent JWT>",...}
    echo "  Creating Platform-admin tenant in Credo multi-tenant agent..."
    local create_resp
    create_resp="$(curl -sf --max-time 15 -X POST \
      -H "Authorization: Bearer ${root_jwt}" \
      -H "Content-Type: application/json" \
      -d '{"config":{"label":"Platform-admin"}}' \
      "${endpoint}/multi-tenancy/create-tenant" 2>/dev/null || true)"

    tenant_id="$(printf '%s' "$create_resp" | python3 -c "import sys,json; d=json.load(sys.stdin); print(d.get('id',''))" 2>/dev/null || true)"
    if [ -z "$tenant_id" ]; then
      echo "  Error: Failed to create tenant. Response: $create_resp" >&2
      return 1
    fi
    echo "  Tenant created: $tenant_id"
  else
    echo "  Tenant already exists: $tenant_id"
  fi

  # Get a fresh tenant JWT via get-token (valid for the current Credo secretKey).
  # Response: {"token":"<RestTenantAgent JWT>"} — NOT {"access_token":"..."}.
  local tenant_jwt
  tenant_jwt="$(curl -sf --max-time 10 -X POST \
    -H "Authorization: Bearer ${root_jwt}" \
    "${endpoint}/multi-tenancy/get-token/${tenant_id}" 2>/dev/null \
    | python3 -c "import sys,json; d=json.load(sys.stdin); print(d.get('token', d.get('access_token','')))" 2>/dev/null || true)"

  if [ -z "$tenant_jwt" ]; then
    echo "  Error: Failed to get tenant JWT for tenant ${tenant_id}." >&2
    return 1
  fi

  # Encrypt tenant JWT with CryptoJS AES (same as CREDEBL's dataEncryption method)
  local encrypted_jwt
  encrypted_jwt="$(docker compose exec -T agent-service node -e "
const CryptoJS = require('crypto-js');
const token = '${tenant_jwt}';
const key = process.env.CRYPTO_PRIVATE_KEY;
const encrypted = CryptoJS.AES.encrypt(JSON.stringify(token), key).toString();
process.stdout.write(encrypted);
" 2>/dev/null)"

  if [ -z "$encrypted_jwt" ]; then
    echo "  Error: Failed to encrypt tenant JWT." >&2
    return 1
  fi

  # Update DB: set tenantId and apiKey
  docker compose exec -T postgres env PGPASSWORD="$POSTGRES_PASSWORD" \
    psql -U credebl -d credebl -v ON_ERROR_STOP=1 -c "
      UPDATE org_agents oa
      SET \"tenantId\" = '${tenant_id}',
          \"apiKey\"   = '${encrypted_jwt}'
      FROM organisation o
      WHERE oa.\"orgId\" = o.id
        AND o.name = 'Platform-admin';" >/dev/null

  echo "  Platform-admin tenant wallet configured (tenantId=${tenant_id})."

  # Restart agent-service so it picks up the new apiKey on next request
  # Restart agent-service so it picks up the refreshed apiKey on the next request.
  # Give it 20s for Nest bootstrap + Prisma migration + NATS reconnect.
  docker compose restart agent-service >/dev/null
  sleep 20
  echo "  Agent-service restarted."
}

